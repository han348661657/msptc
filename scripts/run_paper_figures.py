def _fig_F5_annual_comparison(outdir, pdf=True):
    """Paper A Figure 5: 年度净发电量对比 (晴空/TMY × 海平面/高原 × 两介质)。"""
    _setup_submission_font()
    cases = [
        ("Clear-sky\nSea Level",
         _first_existing(outdir, "Table_04_system_annual_clearsky_sea_level.csv",
                         "system_annual_clearsky_sea_2023.csv")),
        ("Clear-sky\nAltitude",
         _first_existing(outdir, "Table_05_system_annual_clearsky_golmud.csv",
                         "system_annual_clearsky_alt_2023.csv")),
        ("TMY\nSea Level",
         _first_existing(outdir, "Table_06_system_annual_tmy_sea_level.csv",
                         "system_annual_tmy_sea_2023.csv")),
        ("TMY\nAltitude",
         _first_existing(outdir, "Table_07_system_annual_tmy_golmud.csv",
                         "system_annual_tmy_alt_2023.csv")),
    ]
    missing = [p for _, p in cases if not os.path.exists(p)]
    if missing:
        raise RuntimeError(_missing_annual_tables_message(outdir, missing))

    x = np.arange(len(cases))
    width = 0.35
    fig, ax = plt.subplots(figsize=(8, 4.5))
    for i, (htf_type, label, _, color) in enumerate(_LABELS):
        vals = []
        for _, path in cases:
            df = pd.read_csv(path)
            df.columns = df.columns.str.strip("﻿")
            row = df[df["htf_type"] == htf_type]
            vals.append(row["P_elec_MWh"].values[0] if len(row) else 0.0)
        bars = ax.bar(x + (i - 0.5) * width, vals, width, color=color,
                      label=label, edgecolor="white")
        for bar, v in zip(bars, vals):
            ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 8,
                    f"{v:.0f}", ha="center", va="bottom", fontsize=8)
    ax.set_xticks(x)
    ax.set_xticklabels([c for c, _ in cases])
    ax.set_ylabel("Annual Net Electricity Generation (MWh)")
    ax.set_title("Annual Net Electricity: Clear-sky / TMY  ×  Sea Level / Altitude (Golmud)")
    ax.legend(); ax.grid(True, alpha=0.3, axis="y")
    return _save(fig, "Figure_05_annual_comparison.png", outdir, pdf)


def _fig_F8_value_heatmap(df_val, outdir, pdf=True):
    """Paper A Figure 8: 储热价值面热力图 — LCOE 与价值因子 (2 介质 × 储热小时数)。"""
    _setup_submission_font()
    htfs = ["solar_salt", "therminol_vp1"]
    htf_labels = ["Solar Salt", "Therminol VP-1"]
    hours = sorted(df_val["storage_hours"].unique())

    def _matrix(col):
        M = np.zeros((len(htfs), len(hours)))
        for i, h in enumerate(htfs):
            for j, s in enumerate(hours):
                v = df_val[(df_val["htf_type"] == h) & (df_val["storage_hours"] == s)][col]
                if len(v):
                    M[i, j] = v.values[0]
        return M

    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(10, 3))
    for ax, col, cmap, title, fmt in [
        (ax1, "LCOE_CNY_MWh", "RdYlGn_r", "LCOE (CNY/MWh)", "{:.0f}"),
        (ax2, "value_factor",  "RdYlGn",   "Value Factor",    "{:.3f}"),
    ]:
        M = _matrix(col)
        im = ax.imshow(M, cmap=cmap, aspect="auto")
        fig.colorbar(im, ax=ax, shrink=0.9)
        ax.set_xticks(range(len(hours))); ax.set_xticklabels([f"{h}h" for h in hours])
        ax.set_yticks(range(len(htf_labels))); ax.set_yticklabels(htf_labels)
        ax.set_xlabel("Storage Hours (h)"); ax.set_title(title)
        for i in range(len(htfs)):
            for j in range(len(hours)):
                ax.text(j, i, fmt.format(M[i, j]), ha="center", va="center",
                        fontsize=9, color="black")
    fig.suptitle("Storage × HTF Value Surface (LCOE and Value Factor)", fontsize=10)
    return _save(fig, "Figure_08_value_heatmap.png", outdir, pdf)


def _first_existing(outdir, preferred, legacy):
    preferred_path = os.path.join(outdir, preferred)
    if os.path.exists(preferred_path):
        return preferred_path
    return os.path.join(outdir, legacy)


def _fig_F6_lcoe_vs_storage(df_val, outdir, pdf=True):
    """Paper A Figure 6: idealized clear-sky LCOE mechanism, freeze excluded.

    Top panel shows the two LCOE curves; bottom panel shows the salt-oil cost
    difference on its own axis, avoiding a dual-y plot.
    """
    _setup_submission_font()
    fig, (ax_lcoe, ax_delta) = plt.subplots(
        2, 1, figsize=(7.0, 5.0), sharex=True,
        gridspec_kw={"height_ratios": [3.0, 1.15], "hspace": 0.08},
    )
    curves = {}
    for htf, label, mk, color in _LABELS:
        d = df_val[df_val["htf_type"] == htf].sort_values("storage_hours")
        xs = d["storage_hours"].values
        ys = d["LCOE_CNY_MWh"].values
        curves[htf] = (xs, ys)
        ax_lcoe.plot(xs, ys, marker=mk, color=color, lw=1.8, label=label)
        i_min = int(np.argmin(ys))
        ax_lcoe.annotate(f"min {ys[i_min]:.0f}",
                         (xs[i_min], ys[i_min]),
                         textcoords="offset points",
                         xytext=(0, -18 if htf == "solar_salt" else 10),
                         ha="center", fontsize=8, color=color)

    xs = curves["solar_salt"][0]
    delta = curves["solar_salt"][1] - curves["therminol_vp1"][1]
    bar_colors = ["#4C78A8" if v <= 0 else "#C44E52" for v in delta]
    ax_delta.bar(xs, delta, width=0.55, color=bar_colors, alpha=0.68)
    ax_delta.axhline(0, color="0.35", lw=0.8)

    ax_lcoe.set_ylabel("LCOE (CNY/MWh)")
    ax_delta.set_ylabel("Salt - Oil\n(CNY/MWh)")
    ax_delta.set_xlabel("Thermal-storage hours (h)")
    ax_delta.set_xticks(xs)
    ax_lcoe.legend(fontsize=8, loc="upper left", framealpha=0.92)
    ax_lcoe.text(0.98, 0.94, "Idealized clear-sky; freeze excluded",
                 transform=ax_lcoe.transAxes, ha="right", va="top",
                 fontsize=8, color="0.35")
    for ax in (ax_lcoe, ax_delta):
        ax.grid(True, alpha=0.25, axis="y")
    return _save(fig, "Figure_06_lcoe_vs_storage.png", outdir, pdf)


