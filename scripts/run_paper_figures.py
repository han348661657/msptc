def _fig_F11_altitude_synergy(df_ap, outdir, pdf=True, config_path="config/default.json"):
    """Paper B Figure 3 (协同主图, 纯光学通道): 海拔-风载协同——截距因子从海平面到格尔木的增益 Δγ
    随开口增大而单调增大,即"大开口在高原不成比例受益"。

    固定吸热管 70mm + 熔盐, 仅开口变化, 隔离纯几何/光学效应;
    热-海拔通道(对流热损/效率)由 companion paper A 的 F1/F4 承担, 此处不混入。
    """
    _setup_submission_font()
    from msptc.atmosphere import AirProperties
    from msptc.optics.intercept import intercept_factor, sigma_total
    from msptc.optics.wind_deflection import sigma_slope_wind

    apertures = [5.77, 7.5, 8.6]
    focals = {5.77: 1.71, 7.5: 2.23, 8.6: 2.56}
    d = df_ap[(df_ap["htf_type"] == "solar_salt") & (df_ap["d_abs_out_m"] == 0.070)]

    def _g(W, elev):
        s = d[(d["aperture_m"] == W) & (d["elev_m"] == elev)]["gamma"]
        return s.values[0] if len(s) else float("nan")

    central = np.array([(_g(W, 2801) - _g(W, 0)) * 1e3 for W in apertures])

    cfg = load_config(config_path)
    eb = cfg["optics"]["error_budget"]
    wind_cfg = cfg["wind"]
    sun = eb["sigma_sun_mrad"] * 1e-3
    slope_grav = eb["sigma_slope_grav_mrad"] * 1e-3
    spec = eb["sigma_spec_mrad"] * 1e-3
    track = eb["sigma_track_mrad"] * 1e-3
    v_design = wind_cfg["v_design_ms"]
    t_design = wind_cfg.get("t_amb_design_C", 20.0)
    w_ref = wind_cfg["w_ref_m"]

    def _wind_loaded_gamma(W, elev, c_w, aperture_exp):
        rho = AirProperties(elev).rho(t_design)
        slope_wind = sigma_slope_wind(rho, v_design, W, c_w, aperture_exp, w_ref)
        slope = np.sqrt(slope_grav ** 2 + slope_wind ** 2)
        st = sigma_total(sun, slope, spec, track)
        return intercept_factor(W, focals[W], 0.070, st)

    band = []
    for W in apertures:
        vals = []
        for c_w in (1.0e-5, 1.7e-5, 2.5e-5):
            for aperture_exp in (1.0, 1.5, 2.0):
                vals.append((_wind_loaded_gamma(W, 2801, c_w, aperture_exp)
                             - _wind_loaded_gamma(W, 0, c_w, aperture_exp)) * 1e3)
        band.append(vals)
    band = np.array(band)
    lower = band.min(axis=1)
    upper = band.max(axis=1)

    fig, ax = plt.subplots(figsize=(6.8, 4.1))
    ax.fill_between(apertures, lower, upper, color="#C44E52", alpha=0.18,
                    linewidth=0, label="Sensitivity band (9 wind-load cases)")
    ax.plot(apertures, central, marker="D", color="#C44E52", lw=2.0,
            label="Central coefficients")
    for W, dg in zip(apertures, central):
        ax.annotate(f"+{dg:.1f}", (W, dg), textcoords="offset points",
                    xytext=(0, 8), ha="center", fontsize=8, color="#8B1A1A")
    ax.set_xticks(apertures)
    ax.set_xticklabels(["5.77\nEuroTrough", "7.50\nUltimate", "8.60\nlarge aperture"])
    ax.set_xlabel("Aperture width (m)")
    ax.set_ylabel("Altitude gain Δγ (x 10^-3)")
    ax.set_xlim(5.55, 8.82)
    ax.set_ylim(0, max(upper) * 1.12)
    ax.grid(True, alpha=0.28, axis="y")
    ax.legend(fontsize=8, loc="upper left", framealpha=0.92)
    ax.text(0.98, 0.05, "70 mm absorber; wind-loaded intercept difference",
            transform=ax.transAxes, ha="right", va="bottom", fontsize=8, color="0.35")
    return _save(fig, "Figure_03_altitude_synergy.png", outdir, pdf)


