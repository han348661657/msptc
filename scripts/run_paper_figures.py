def _fig_geometry_schematic(outdir, pdf=True):
    """Paper B Figure 1 (schematic, not to scale): parabolic-trough optics —
    aperture W, focal length f, rim angle φ_r, absorber tube, and the local
    acceptance half-angle Δθ versus the optical error cone that defines γ.

    Pure illustration (no simulation data); proportions chosen for a rim angle
    of ~80° and an exaggerated absorber radius for legibility.
    """
    import math as _m
    from matplotlib.patches import Arc, Polygon, Circle
    _setup_submission_font()

    f = 1.0
    W = 3.357                       # -> rim angle ~80°
    half = W / 2.0
    xs = np.linspace(-half, half, 400)
    ys = xs ** 2 / (4.0 * f)
    F = np.array([0.0, f])
    y_rim = half ** 2 / (4.0 * f)

    fig, ax = plt.subplots(figsize=(6.8, 3.8))
    ax.plot(xs, ys, color="#22507A", lw=2.6, zorder=3)          # mirror
    ax.plot([0, 0], [0, f], color="gray", ls=":", lw=1.0)       # optical axis

    R = 0.14                                                     # exaggerated absorber radius
    ax.add_patch(Circle(F, R, fill=False, ec="#C0392B", lw=2.0, zorder=6))
    ax.plot(*F, marker="+", color="#C0392B", ms=9, mew=1.8, zorder=7)

    # aperture width arrow (above the absorber to avoid clutter)
    ya = f + 0.34
    ax.annotate("", xy=(half, ya), xytext=(-half, ya),
                arrowprops=dict(arrowstyle="<->", color="black", lw=1.2))
    ax.text(0, ya + 0.05, "Aperture W", ha="center", va="bottom", fontsize=11)
    ax.plot([-half, -half], [y_rim, ya], color="gray", lw=0.6, ls="--")
    ax.plot([half, half], [y_rim, ya], color="gray", lw=0.6, ls="--")

    # focal length arrow
    ax.annotate("", xy=(0, f), xytext=(0, 0),
                arrowprops=dict(arrowstyle="<->", color="dimgray", lw=1.1))
    ax.text(0.05, f / 2, "f", ha="left", va="center", fontsize=11, color="dimgray")

    # rim angle
    Prim = np.array([half, y_rim])
    ax.plot([F[0], Prim[0]], [F[1], Prim[1]], color="#7F8C8D", lw=1.0)
    ang_axis = _m.degrees(_m.atan2(0 - f, 0.0))                 # -90 (downward)
    ang_rim = _m.degrees(_m.atan2(Prim[1] - f, Prim[0]))
    ax.add_patch(Arc(F, 0.9, 0.9, angle=0,
                     theta1=min(ang_axis, ang_rim), theta2=max(ang_axis, ang_rim),
                     color="#7F8C8D", lw=1.2))
    ax.text(0.27, f - 0.46, "φr", fontsize=13, color="#566573")

    # mirror element: incident ray, reflected ray, acceptance cone, error cone
    Px = 1.30
    P = np.array([Px, Px ** 2 / (4.0 * f)])
    ax.annotate("", xy=(P[0], P[1]), xytext=(P[0], y_rim + 0.38),
                arrowprops=dict(arrowstyle="->", color="#E67E22", lw=1.6))
    ax.text(P[0] + 0.04, y_rim + 0.34, "incident\nrays", fontsize=8,
            color="#E67E22", va="top")

    d = F - P
    dist = float(np.linalg.norm(d))
    u = d / dist
    perp = np.array([-u[1], u[0]])
    e1, e2 = F + R * perp, F - R * perp
    ax.add_patch(Polygon([P, e1, e2], closed=True, facecolor="#BDC3C7",
                         alpha=0.45, edgecolor="none", zorder=2))         # acceptance
    ax.plot([P[0], F[0]], [P[1], F[1]], color="#C0392B", lw=1.3, zorder=4)

    sig = _m.radians(4.2)
    base = _m.atan2(u[1], u[0])
    a1, a2 = base + sig, base - sig
    ax.add_patch(Polygon([P, P + dist * np.array([_m.cos(a1), _m.sin(a1)]),
                          P + dist * np.array([_m.cos(a2), _m.sin(a2)])],
                         closed=True, facecolor="#E67E22", alpha=0.32,
                         edgecolor="none", zorder=2))                     # error cone
    ax.text(P[0] - 0.42, P[1] + 0.20, "acceptance Δθ", fontsize=10, color="#34495E")
    ax.text(0.68, 0.52, "error cone σtot", fontsize=10, color="#B9651B")

    ax.text(F[0] - R - 0.07, F[1] + 0.11, "Absorber D", fontsize=10,
            color="#C0392B", ha="right", va="bottom")

    ax.set_aspect("equal")
    ax.set_xlim(-half - 0.18, half + 0.18)
    ax.set_ylim(-0.12, f + 0.55)
    ax.axis("off")
    return _save(fig, "Figure_00_geometry_schematic.png", outdir, pdf)


